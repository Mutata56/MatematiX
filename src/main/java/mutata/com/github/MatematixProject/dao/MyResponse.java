package mutata.com.github.MatematixProject.dao;

import java.util.List;

/**
 * Универсальный контейнер для результатов запросов к базе данных.
 * <p>Служит для упаковки списка сущностей и общего количества записей,
 * что упрощает передачу данных в сервисных и контроллерных слоях.</p>
 *
 * @param <T> тип элементов в списке содержимого
 */
public class MyResponse<T> {

    /**
     * Общее количество элементов (например, число всех записей в базе).
     */
    private long total;

    /**
     * Непосредственный список элементов текущей страницы или выборки.
     */
    private List<T> content;

    /**
     * Конструктор для создания экземпляра контейнера.
     *
     * @param content список элементов
     * @param total   общее количество элементов
     */
    public MyResponse(List<T> content, long total) {
        this.content = content;
        this.total = total;
    }

    /**
     * Возвращает общее количество элементов, которое соответствует суммарному числу записей,
     * а не только числу в текущем списке.
     *
     * @return общее число элементов в выборке
     */
    public long getTotal() {
        return total;
    }

    /**
     * Возвращает список элементов в рамках текущего запроса или страницы.
     *
     * @return список элементов типа {@code T}
     */
    public List<T> getContent() {
        return content;
    }

    /**
     * Устанавливает новое значение общего количества элементов.
     * <p>Метод используется внутренне и не предназначен для публичного API.</p>
     *
     * @param total новое общее количество элементов
     */
    void setTotal(int total) {
        this.total = total;
    }

    /**
     * Устанавливает новый список содержимого.
     * <p>Метод используется внутренне и не предназначен для публичного API.</p>
     *
     * @param content новый список элементов
     */
    void setContent(List<T> content) {
        this.content = content;
    }
}
